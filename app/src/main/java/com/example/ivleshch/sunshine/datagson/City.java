public class City {
    private Coord coord;

    private String id;

    private String name;

    private String population;

    private String country;

    public Coord getCoord ()
    {
        return coord;
    }

    public String getId ()
    {
        return id;
    }

    public String getName ()
    {
        return name;
    }

    public String getPopulation ()
    {
        return population;
    }

    public String getCountry ()
    {
        return country;
    }
}
